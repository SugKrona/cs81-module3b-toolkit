// double a number
function double(n) {
  return n * 2;
}
// square a number
function square(n) {
  return n * n;
}
// check if a number is even
function isEven(n) {
  return n % 2 === 0;
}
// check if a number is odd
function isOdd(n) {
  return n % 2 !== 0;
}
